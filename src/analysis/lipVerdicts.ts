/**
 * Doctor-written, plain-language conclusions for each lip parameter.
 *
 * For every measured parameter we store three states — norm / below norm /
 * above norm — each with:
 *   - `verdict`: a one-line conclusion shown ALWAYS-visible under the metric
 *                (front and center, with a colour/icon), so the client sees
 *                what deviates from the norm without opening anything.
 *   - `detail`:  the full doctor explanation revealed inside "Подробнее"
 *                (above "Что измеряется"): why it matters and what a doctor
 *                can do about it, in non-medical language.
 *
 * Source: clinic-provided copy (ProFace doctors). Russian only — the product's
 * primary audience. For other languages the bar falls back to its existing
 * generic "how to read / why important" text.
 */

export type LipVerdictState = 'ideal' | 'below' | 'above';

export interface LipVerdict {
  /** Short headline conclusion (always visible). */
  verdict: string;
  /** Full doctor explanation (revealed in "Подробнее"). */
  detail: string;
}

export type LipVerdictSet = Record<LipVerdictState, LipVerdict>;

