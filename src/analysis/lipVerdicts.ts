/**
 * Resolve the doctor verdict for a measured lip parameter.
 *
 * @param key       proportion key (e.g. `upperLowerRatio`)
 * @param status    'ideal' | 'close' | 'deviation' from computeProportions
 * @param userValue measured value
 * @param idealMin  lower bound of the ideal range
 * @param idealMax  upper bound of the ideal range
 * @param lang      current UI language
 * @returns the matching verdict, or null if no copy exists / non-RU
 */
export function getLipVerdict(
  key: string,
  status: 'ideal' | 'close' | 'deviation',
  userValue: number,
  idealMin: number,
  idealMax: number,
  lang: 'ru' | 'en',
): LipVerdict | null {
  if (lang !== 'ru') return null;
  const set = LIP_VERDICTS_RU[key];
  if (!set) return null;

  if (status === 'ideal') return set.ideal;
  // Below or above the ideal band — pick by side of the deviation.
  return userValue < idealMin ? set.below : set.above;
}
